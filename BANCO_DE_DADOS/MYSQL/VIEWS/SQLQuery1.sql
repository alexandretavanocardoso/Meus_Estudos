-- CRIANDO
CREATE VIEW View_Invoca AS
SELECT C.NOME, 
	C.SEXO, 
	C.EMAIL, 
	N.NUMERO, 
	E.BAIRRO, 
	E.CIDADE, 
	E.ESTADO
FROM CLIENTE C
INNER JOIN ENDERECO E
ON C.IDCLIENT = E.IDCLIENT
INNER JOIN NUMERO N
ON C.IDCLIENT = N.IDCLIENT

-- CHAMANDO
SELECT * FROM View_Invoca

-- CHAMANDO COM ALGUNS DADOS
SELECT NOME, NUMERO, ESTADO FROM View_Invoca

-- MOSTRANDO 
SHOW TABLES;

-- APAGANDO
DROP VIEW View_Invoca
