

/* MODIFICADORES DE ACESSOS  */

public - Permite ser acessado em qualquer tipo de classe
protected internal - Permite acessar qualquer tipo de classe, menos uma classe em outro projeto
internal - propria classe e classe no mesmo projeto
protected - propria classe e subclasses no mesmo e em outro projeto
private protected - propria classe e Subclasse do mesmo projeto
private - Apenas a propria classe pode acessar
