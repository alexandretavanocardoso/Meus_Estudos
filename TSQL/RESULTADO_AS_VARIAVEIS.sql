-- DECLARANDO VARIAVEL
DECLATE 
		@V_CONT_FORD INT,
		@v_CONT_FIAT INT
BEGIN
	-- METODO 1 - O SELECT PRECISA RETORNAR UM SIMPLES COLUNA
	-- E UM SÃ“ RESULTADO
	SET @V_CONT_FORD = (SELECT COUNT(*) FROM CARROS
	WHERE FABRICANTE = 'FORD')
	
	-- MOTODO 2
	SELECT @v_CONT_FIAT = COUNT(*) FROM CARROS 
	WHERE FABRICANTE = 'FIAT'
	
	
END
GO






