

/* OPERAÇÕES DE ATRIBUTOS  */

			--------------------------------|
			| Operadores|  Significado 		|
			|-------------------------------|
			|	 >		| 	Maior			| 
			|	 <	    | 	Menor			|
			|	 >=		| 	Maior ou Igual	| 
			|	 <=		| 	Menor ou Igual  |
			|	 !=		| 	Diferente		|
			|    ==     | 	Igual			|
			---------------------------------
			
/* Exemplo Avançados */

int a = 10;
bool c1 = a < 10; // false
bool c2 = a > 5; // true
bool c3 = a == 10; // true
bool c4 = a != 30; // true
bool c5 = a >= 8; // true
bool c6 = a <= 7; // false

Console.WriteLine(c1);
Console.WriteLine(c2);
Console.WriteLine(c3);
Console.WriteLine(c4);
Console.WriteLine(c5);
Console.WriteLine(c6);

