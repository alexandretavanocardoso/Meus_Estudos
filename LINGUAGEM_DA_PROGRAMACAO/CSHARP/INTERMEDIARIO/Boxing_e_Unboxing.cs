
/* BOXING E UNBOXING */

-- BOXING

- É o processo de conversão de um objeto tipo valor para um objeto tipo
referencia compátivel

EX.

int x = 20;

// Obejct - Compativel com qualquer tipo
Object obj = x;

-- UNBOXING

- É o processo de conversão de um objeto tipo referencia para um objeto
tipo valor compativel

int x = 20;

// Obejct - Compativel com qualquer tipo
Object obj = x;

int y = (int) obj;